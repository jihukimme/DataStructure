//특정 정점에서 시작하는 최소비용 신장트리 
//prim알고리즘
//선택된 정점인지 확인하는 알고리즘


#include <stdio.h>
#include <stdlib.h>

#define TRUE 1
#define FALSE 0
#define MAX_VERTICES 100
#define INF 1000

typedef struct GraphType {
	int n;	// 정점의 개수
	int weight[MAX_VERTICES][MAX_VERTICES];
} GraphType;

int selected[MAX_VERTICES];
int distance[MAX_VERTICES];

// 최소 dist[v] 값을 갖는 정점을 반환
int get_min_vertex(int n){ //n은 정점개수
	int v, i; 
	for (i = 0; i < n; i++)
		if (!selected[i]) { //i가 선택된게 아니면
			v = i;
			break;
		}
	for (i = 0; i < n; i++) {
		if (!selected[i] && (distance[i] < distance[v])) //i가 선택된게 아니고 v의 거리보다 작다면
			v = i; 
	}
	return (v);
}


void prim(GraphType* g, int s){
	int i, u, v;

	for (u = 0; u < g->n; u++)
		distance[u] = INF; //거리 초기화
	distance[s] = 0; //시작정점 거리를 0으로

	for (i = 0; i < g->n; i++) {
		u = get_min_vertex(g->n); //u에 최소거리 정점을 저장
		selected[u] = TRUE; //u를 선택

		if (distance[u] == INF) 
			return;
		
		printf("정점 %d 추가\n", u);
		for (v = 0; v < g->n; v++) {
			if (g->weight[u][v] != INF) {
				if (!selected[v] && g->weight[u][v] < distance[v]) {
					distance[v] = g->weight[u][v];
				}
			}
		}

		//select출력
		printf("selected[%d]: ", u);
		for (int j = 0; j < g->n; j++) {
			printf("%d ", selected[j]);
		}

		//distance출력
		printf("\ndistance[%d]: ", u);
		for (int j = 0; j < g->n; j++) {
			if (distance[j] == INF)
				printf("INF ");
			else
				printf("%d ", distance[j]);
		}
		printf("\n");
		printf("\n");
	}
}

int main(void){
	GraphType g = { 7,
	{{ 0, 29, INF, INF, INF, 10, INF },
	{ 29,  0, 16, INF, INF, INF, 15 },
	{ INF, 16, 0, 12, INF, INF, INF },
	{ INF, INF, 12, 0, 22, INF, 18 },
	{ INF, INF, INF, 22, 0, 27, 25 },
	{ 10, INF, INF, INF, 27, 0, INF },
	{ INF, 15, INF, 18, 25, INF, 0 } }
	};

	GraphType g2 = { 7,
	{{ 0,  7,  INF, INF,   3,  10, INF },
	{ 7,  0,    4,  10,   2,   6, INF },
	{ INF,  4,    0,   2, INF, INF, INF },
	{ INF, 10,    2,   0,  11,   9,   4 },
	{ 3,  2,  INF,  11,   0, INF,   5 },
	{ 10,  6,  INF,   9, INF,   0, INF },
	{ INF, INF, INF,   4,   5, INF,   0 } }
	};


	prim(&g2, 1);

	return 0;
}